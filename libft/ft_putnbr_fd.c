#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	num;
	int			count;

	num = n;
	count = 0;
	if (num < 0)
	{
		write(fd, "-", 1);
		num *= -1;
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + 48, fd);
}
